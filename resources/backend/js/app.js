require('./bootstrap');

window.uploadImage = function (file, callBackSuccess) {
    const formData = new FormData();
    formData.append("img_file", file);
    let apiToken = $('meta[name=api-token]').attr('content');
    axios.post(
        '/api/upload/image?api_token=' + apiToken,
        formData,
        {headers: {'Content-Type': 'multipart/form-data'}}
        )
        .then(function (response) {
                console.log(response.data);
                if (response.data.success) {
                    callBackSuccess(response.data.data);
                } else {
                    alert('Upload ảnh lỗi!!!');
                }
            })
        .catch(function (error) {
            console.log(error);
            alert('Upload ảnh lỗi!!!');
        });
};

window.makeSlug = function(str) {
    // Chuyển hết sang chữ thường
    str = str.toLowerCase();

    // xóa dấu
    str = str.replace(/(à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ)/g, 'a');
    str = str.replace(/(è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ)/g, 'e');
    str = str.replace(/(ì|í|ị|ỉ|ĩ)/g, 'i');
    str = str.replace(/(ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ)/g, 'o');
    str = str.replace(/(ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ)/g, 'u');
    str = str.replace(/(ỳ|ý|ỵ|ỷ|ỹ)/g, 'y');
    str = str.replace(/(đ)/g, 'd');

    // Xóa ký tự đặc biệt
    str = str.replace(/([^0-9a-z-\s])/g, '');

    // Xóa khoảng trắng thay bằng ký tự -
    str = str.replace(/(\s+)/g, '-');

    // xóa phần dự - ở đầu
    str = str.replace(/^-+/g, '');

    // xóa phần dư - ở cuối
    str = str.replace(/-+$/g, '');

    // return
    return str;
};
